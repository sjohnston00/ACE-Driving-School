        //
        // POST: /Account/Register
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Register(RegisterViewModel model)
        {
            if (ModelState.IsValid)
            {
                var user = new Student 
                { 
                    UserName = model.Username, 
                    Email = model.Email,
                    AddressLine1 = model.AddressLine1,
                    AddressLine2 = model.AddressLine2,
                    City = model.City,
                    Postcode = model.Postcode,
                    First_Name = model.Firstname,
                    Last_Name = model.Lastname,
                    PhoneNumber = model.PhoneNumber,
                    Date_Of_Birth = model.Date_Of_Birth,
                    DrivingLicenseNo = model.DrivingLicenseNo
                };
                var result = await UserManager.CreateAsync(user, model.Password);

                //find the student and assign him to the student role
                Student student = (Student)await UserManager.FindByEmailAsync(model.Email);
                var AddingToRole = await UserManager.AddToRoleAsync(student.Id, "Student");

                if (result.Succeeded && AddingToRole.Succeeded)
                {
                    
                    await SignInManager.SignInAsync(user, isPersistent:false, rememberBrowser:false);

                    string code = await UserManager.GenerateEmailConfirmationTokenAsync(user.Id);
                    var callbackUrl = Url.Action("ConfirmEmail", "Account", new { userId = user.Id, code = code }, protocol: Request.Url.Scheme);
                    var message = new IdentityMessage
                    {
                        Destination = user.Email,
                        Body = $"Thank you for signing up to our app \n Please confirm your account by clicking this link: \n{callbackUrl}",
                        Subject = "Please Confirm Email"
                    };

                    SendEmail(message);

                    return RedirectToAction("Index", "Home");
                }
                AddErrors(result);
            }

            // If we got this far, something failed, redisplay form
            return View(model);
        }

